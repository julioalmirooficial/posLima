    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        
        if(txtFullName.getText().length() <= 2){
            new rojerusan.RSNotifyAnimated("ERROR", "POR FAVOR INGRESA EL NOMBRE DEL USUARIO",
                        5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                        RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
            return;
        }
        if(txtUserName.getText().length() <= 3){
            new rojerusan.RSNotifyAnimated("ERROR", "POR FAVOR INGRESA EL NOMBRE USUARIO PARA EL ACCESO AL SISTEMA",
                        5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                        RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
            return;
        }
        if(txtNumberDocument.getText().length() <= 7 ||txtNumberDocument.getText().length() > 11){
            new rojerusan.RSNotifyAnimated("ERROR", "POR FAVOR INGRESA EL NUMERO DEL DOCUMENTO DEL USUARIO, Y VUELVE A INTENTARLO",
                        5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                        RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
            return;
        }
        ControllerUser cUser = new ControllerUser();
        ModelUser mUser = new ModelUser();
        mUser.setFullName(txtFullName.getText());
        mUser.setEmail(txtEmail.getText());
        mUser.setPassword(txtPassword.getText());
        mUser.setUserName(txtUserName.getText());
        mUser.setNumberDocument(txtNumberDocument.getText());
        mUser.setTypeUser(cbxTypeUser.getSelectedItem().toString());
        
        if(mUser.getEmail().equals("ERROR")) {
            new rojerusan.RSNotifyAnimated("ERROR", "POR FAVOR INGRESA UN CORREO VALIDO, Y VUELVE A INTENTARLO",
                        5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                        RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
            return;
        }
        if(mUser.getPassword().equals("ERROR")) {
            new rojerusan.RSNotifyAnimated("ERROR", "LA CONTRASEÑA QUE INGRESASTE ES MENOR A 4 CARACTERES INGRESE UN CONTRASEÑA MAS SEGURAS",
                        5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                        RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
            return;
        }
        if(mUser.getTypeUser().equals("ERROR")) {
            new rojerusan.RSNotifyAnimated("ERROR", "POR FAVOR SELECCIONA UN TIPO DE USUARIO PARA PODER REALIZAR EL REGISTRO",
                        5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                        RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
            return;
        }
        try {
            if(accion.equals("savedata")) {
                if (cUser.insertUser(mUser)) {
                    new rojerusan.RSNotifyAnimated("FELICIDADES", "HAS REGISTRADO UN USUARIO CON EL MONBRE DE " + txtFullName.getText(),
                            5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                            RSNotifyAnimated.TypeNotify.SUCCESS).setVisible(true);
                    getUser();
                    ConnectionDB cn = new ConnectionDB();
                    cn.closeConection();
                    clearImputs();
                } else {
                    new rojerusan.RSNotifyAnimated("ERROR", "NO HEMOS PODIDO REGISTRAR AL USUARIO, INTENTALO MAS TARDE",
                            5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                            RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
                }
            } else if(accion.equals("updatedata")) {
                mUser.setId(Integer.parseInt(idUpdateData.getText()));
                if (cUser.updateUser(mUser)) {
                    new rojerusan.RSNotifyAnimated("FELICIDADES", "LOS DATOS DEL USARIO" + txtFullName.getText() + "SE HAN ACTUALIZADO CON ÉXITO",
                            5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                            RSNotifyAnimated.TypeNotify.SUCCESS).setVisible(true);
                    getUser();
                    ConnectionDB cn = new ConnectionDB();
                    cn.closeConection();
                } else {
                    new rojerusan.RSNotifyAnimated("ERROR", "NO HEMOS PODIDO ACTUALIZAR LOS DATOS DEL USUARIO, INTENTALO MAS TARDE",
                            5, RSNotifyAnimated.PositionNotify.BottomRight, RSNotifyAnimated.AnimationNotify.RightLeft,
                            RSNotifyAnimated.TypeNotify.ERROR).setVisible(true);
                }
                this.dispose();
            }
        } catch (SQLException e) {
        }

    }//GEN-LAST:event_btnRegisterActionPerformed

