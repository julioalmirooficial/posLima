    private void listProductsMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listProductsMousePressed
        if (evt.getClickCount() == 2) {
            int fila = listProducts.getSelectedRow();
            String[] data = new String[WindowSales.headers.length];

            

            componentsregistry.QuantitySales.discount = listProducts.getValueAt(fila, 5).toString(); // DEXCEUNTO
            new componentsregistry.QuantitySales(null, true).setVisible(true);
            if(Double.parseDouble(pQuantity) <= 0) {
                return;
            }
            
            double total = Double.parseDouble(listProducts.getValueAt(fila, 4).toString()) * Double.parseDouble(pQuantity);
            double discounts = Double.parseDouble(discount) / 100;
            double discountAplicate = total * discounts;
            double newPrice = total - discountAplicate;
            double priceForSale = Math.round(newPrice * 100)/100d;
            double subTotal =  priceForSale/ 1.18;
            double igv = subTotal * 0.18;
            data[0] = listProducts.getValueAt(fila, 0).toString();
            data[1] = pQuantity;
            data[2] = listProducts.getValueAt(fila, 2).toString();
            data[3] = "NIU";
            data[4] = discount;
            data[5] = listProducts.getValueAt(fila, 4).toString();
            data[6] = String.valueOf(Math.round(igv * 100)/100d);//IGV
            data[7] = String.valueOf(Math.round(subTotal * 100)/100d);//SUBTOTAL
            data[8] = String.valueOf(priceForSale);//TOTAL
            data[9] = listProducts.getValueAt(fila, 6).toString();//TOTAL
            
            WindowSales.modelTable.addRow(data);
            calculateTotal();
            pQuantity = "0";
            discount = "0";
        }
    }//GEN-LAST:event_listProductsMousePressed

