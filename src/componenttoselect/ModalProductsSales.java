    void calculateTotal() {
        
        double total = 0;      
        for (int i = 0; i < WindowSales.listProducts.getRowCount(); i++) {
             total +=  Double.parseDouble(WindowSales.listProducts.getValueAt(i, 8).toString());
        }
        double subTotal = total / 1.18;
        double igv = subTotal * 0.18;
        WindowSales.lblTotal.setText(String.valueOf(Math.round(total * 100) / 100d));
        WindowSales.lblSubTotal.setText(String.valueOf(Math.round(subTotal * 100)/100d));
        WindowSales.lblIGV.setText(String.valueOf(Math.round(igv * 100)/100d));
    }

